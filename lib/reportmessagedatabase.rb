    puts "Posts: #{rows.next[0]}"
    statement.close

    statement = @db.prepare 'select count(*)
                               from reportmessages
                              where reported_content_type = \'comment\''
    rows = statement.execute
    puts "Comments: #{rows.next[0]}"
    statement.close

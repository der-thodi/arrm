  def print_subreddit_details
    subreddits = Array.new

    statement = @db.prepare 'select distinct subreddit
                               from reportmessages'
    rows = statement.execute
    while (row = rows.next)
      subreddits << row[0]
    end
    statement.close

    subreddits.each { |sub|
      statement = @db.prepare 'select distinct reported_account
                                 from reportmessages
                                where subreddit = ?
                                  and violation = \'yes\''
      statement.bind_params sub
      rows = statement.execute
      puts "Sub: #{sub}"
      while (row = rows.next)
        print "#{row[0]} "
      end
      puts
      statement.close
    }

